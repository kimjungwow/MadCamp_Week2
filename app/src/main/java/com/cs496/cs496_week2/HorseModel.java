package com.cs496.cs496_week2;


public class HorseModel {
    private String name;
    private Number speed, location, acceleration, maxSpeed, fallOff, dividendRate;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Number getSpeed() {
        return speed;
    }

    public void setSpeed(Number speed) {
        this.speed = speed;
    }

    public Number getLocation() {
        return location;
    }

    public void setLocation(Number location) {
        this.location = location;
    }

    public Number getAcceleration() {
        return acceleration;
    }

    public void setAcceleration(Number acceleration) {
        this.acceleration = acceleration;
    }

    public Number getMaxSpeed() {
        return maxSpeed;
    }

    public void setMaxSpeed(Number maxSpeed) {
        this.maxSpeed = maxSpeed;
    }

    public Number getFallOff() {
        return fallOff;
    }

    public void setFallOff(Number fallOff) {
        this.fallOff = fallOff;
    }

    public Number getDividendRate() {
        return dividendRate;
    }

    public void setDividendRate(Number dividendRate) {
        this.dividendRate = dividendRate;
    }

}